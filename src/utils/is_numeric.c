#include "philo.h"

int	is_numeric(char *str)
{
	int	i;

	i = 0;
	while (str && str[i])
	{
		if (i == 0 && (str[i] == '-' || str[i] == '+'))
			i++;
		if (!is_digit(str[i]))
			return (0);
		i++;
	}
	return (1);
}
